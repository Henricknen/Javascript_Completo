let n = 10;     // variável 'n' sendo iniçializada com 10
let fat = 1;

while(n >= 1) {     // enquanto 'while' n for maior ou igual a 1
    fat *= n;       // é exucutado ess instrução 'fat x n'
    n--;        // 'decrementando' n
}

console.log(fat);       // dando console em 'fat' variável que reçeberá o fatorial