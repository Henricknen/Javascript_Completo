let num = 10

if(num > 10) {        // utilizando o 'if' seguida da expressão logica
    console.log("Numero é maior que 10")        // se a expressão for verdadeira
} else if(num > 5) {        // 'else if' equivale 'ou seja'
    console.log("Numero está ente 6 e 10")        
} else {
    console.log("Numero menor ou igual a 5");       // se nehuma das expressões anteriores forem verdadeira será impresso esse 'console'
}

console.log("Fim do programa");